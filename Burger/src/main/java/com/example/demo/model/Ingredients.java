package com.example.demo.model;

public class Ingredients {
	int cheese, bacon, salad;

	public int getCheese() {
		return cheese;
	}

	public void setCheese(int cheese) {
		this.cheese = cheese;
	}

	public int getBacon() {
		return bacon;
	}

	public void setBacon(int bacon) {
		this.bacon = bacon;
	}

	public int getSalad() {
		return salad;
	}

	public void setSalad(int salad) {
		this.salad = salad;
	}

}
